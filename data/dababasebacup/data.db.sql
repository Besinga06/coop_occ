BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS "tbl_beginning_cash" (
	"id"	INTEGER PRIMARY KEY AUTOINCREMENT,
	"amount"	INTEGER,
	"date"	TEXT
);
CREATE TABLE IF NOT EXISTS tbl_capital_share (
    share_id INTEGER PRIMARY KEY AUTOINCREMENT,
    cust_id INTEGER NOT NULL,
    amount NUMERIC NOT NULL,
    contribution_date TEXT DEFAULT CURRENT_TIMESTAMP,
    year INTEGER GENERATED ALWAYS AS (strftime('%Y', contribution_date)) VIRTUAL,
    FOREIGN KEY (cust_id) REFERENCES tbl_customer(cust_id)
);


CREATE TABLE IF NOT EXISTS "tbl_category" (
	"cat_id"	INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
	"category_name"	TEXT
);
CREATE TABLE IF NOT EXISTS "tbl_customer" (
	"cust_id"	INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
	"name"	TEXT,
	"address"	TEXT,
	"contact"	TEXT,
	"field_status"	INTEGER DEFAULT 0,
	"date"	INTEGER DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS "tbl_damage" (
	"damage_id"	INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
	"user_id"	INTEGER,
	"product_id"	INTEGER,
	"date_damage"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"quantity_damage"	INTEGER,
	"notes"	TEXT,
	"field_status"	INTEGER DEFAULT 0,
	"date"	INTEGER DEFAULT CURRENT_TIMESTAMP,
	FOREIGN KEY("product_id") REFERENCES "tbl_products"("product_id"),
	FOREIGN KEY("user_id") REFERENCES "tbl_users"("user_id")
);
CREATE TABLE IF NOT EXISTS "tbl_deposits" (
	"deposit_id"	INTEGER PRIMARY KEY AUTOINCREMENT,
	"user_id"	INTEGER,
	"date_added"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"date"	INTEGER,
	"amount"	REAL,
	"balance"	REAL
);
CREATE TABLE IF NOT EXISTS "tbl_expences" (
	"expences_id"	INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
	"user_id"	INTEGER,
	"date_expence"	INTEGER DEFAULT CURRENT_TIMESTAMP,
	"description"	REAL,
	"expence_amount"	NUMERIC,
	"aprove_by"	TEXT,
	"notes"	TEXT,
	"field_status"	INTEGER DEFAULT 0,
	"date"	INTEGER DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS "tbl_history" (
	"history_id"	INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE,
	"date_history"	TEXT DEFAULT CURRENT_TIMESTAMP,
	"details"	TEXT,
	"history_type"	INTEGER,
	"field_status"	INTEGER DEFAULT 0,
	"date"	INTEGER DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS tbl_loan_application (
    loan_app_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    customer_id      INTEGER NOT NULL,
    requested_amount DECIMAL(12,2) NOT NULL,
    term_months      INTEGER NOT NULL,
    purpose          TEXT,
    status           TEXT DEFAULT 'pending', -- pending, approved, rejected
    application_date DATETIME DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (customer_id) REFERENCES tbl_customer(cust_id)
);
CREATE TABLE IF NOT EXISTS tbl_loan_approval (
    approval_id     INTEGER PRIMARY KEY AUTOINCREMENT,
    loan_app_id     INTEGER NOT NULL,
    approved_amount DECIMAL(12,2) NOT NULL,
    approved_term   INTEGER NOT NULL,
    interest_rate   DECIMAL(5,2) NOT NULL, -- e.g. 12.00 = 12% per year
    officer_id      INTEGER, -- from tbl_users
    approval_date   DATETIME DEFAULT CURRENT_TIMESTAMP,
    remarks         TEXT,
    FOREIGN KEY (loan_app_id) REFERENCES tbl_loan_application(loan_app_id),
    FOREIGN KEY (officer_id) REFERENCES tbl_users(user_id)
);
CREATE TABLE IF NOT EXISTS tbl_loan_disbursement (
    disbursement_id INTEGER PRIMARY KEY AUTOINCREMENT,
    loan_app_id     INTEGER NOT NULL,
    amount_released DECIMAL(12,2) NOT NULL,
    release_date    DATETIME DEFAULT CURRENT_TIMESTAMP,
    mode            TEXT, -- cash, bank, check, mobile wallet
    FOREIGN KEY (loan_app_id) REFERENCES tbl_loan_application(loan_app_id)
);
CREATE TABLE IF NOT EXISTS tbl_loan_fund (
    fund_id INTEGER PRIMARY KEY AUTOINCREMENT,
    fund_name TEXT NOT NULL,                -- e.g., "General Loan Fund"
    starting_balance REAL NOT NULL,         -- initial capital
    current_balance REAL NOT NULL,          -- available fund
    created_at TEXT DEFAULT CURRENT_TIMESTAMP
);
CREATE TABLE IF NOT EXISTS tbl_loan_repayment (
    repayment_id INTEGER PRIMARY KEY AUTOINCREMENT,
    loan_app_id INTEGER NOT NULL,
    amount_paid REAL NOT NULL,              -- usually principal + interest
    principal_component REAL NOT NULL,
    interest_component REAL NOT NULL,
    payment_method TEXT,
    receipt_number TEXT UNIQUE,
    payment_date TEXT DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY(loan_app_id) REFERENCES tbl_loan_application(loan_app_id)
);
CREATE TABLE IF NOT EXISTS tbl_loan_schedule (
    schedule_id     INTEGER PRIMARY KEY AUTOINCREMENT,
    loan_app_id     INTEGER NOT NULL,
    due_date        DATE NOT NULL,
    principal_due   DECIMAL(12,2) NOT NULL,
    interest_due    DECIMAL(12,2) NOT NULL,
    total_due       DECIMAL(12,2) NOT NULL,
    status          TEXT DEFAULT 'unpaid', -- unpaid, paid, overdue
    FOREIGN KEY (loan_app_id) REFERENCES tbl_loan_application(loan_app_id)
);
CREATE TABLE IF NOT EXISTS tbl_loan_settings (
    setting_id      INTEGER PRIMARY KEY AUTOINCREMENT,
    setting_name    TEXT NOT NULL UNIQUE,
    setting_value   TEXT NOT NULL
);
